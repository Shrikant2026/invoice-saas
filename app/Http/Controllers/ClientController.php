    {
        return view('clients.create');
    }

    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
        'name' => 'required',
        'email' => 'nullable|email|unique:clients,email'
        ]);

        # for now we will only allow name and email to be filled, you can add phone and address as well
        #auth()->user()->clients()->create($request->all());
        auth()->user()->clients()->create(
            $request->only(['name', 'email'])
        );

        return redirect()->route('clients.index')->with('success', 'Client added');
    }

